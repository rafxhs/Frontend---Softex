// 14. Faça um programa que exiba os números ímpares de 1 a 100 utilizando o while.

let numerosImpares: number = 1;

while (numerosImpares < 100){

    numerosImpares++

    if(numerosImpares % 2 != 0){

        console.log(numerosImpares);
    }
}



